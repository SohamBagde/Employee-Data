package com.example.demo.Data;

// Reader Class
public class EmployeeInput {
    private String position_ID;
    private String position_Status;
    private String time;
    private String time_Out;
    private String time_Card_Hours;
    private String pay_Cycle_Start_Date;
    private String pay_Cycle_End_Date;
    private String employee_Name;
    private String file_Number;

    // Getters and Setters
    public String getPosition_ID() {
        return position_ID;
    }

    public void setPosition_ID(String position_ID) {
        this.position_ID = position_ID;
    }

    public String getPosition_Status() {
        return position_Status;
    }

    public void setPosition_Status(String position_Status) {
        this.position_Status = position_Status;
    }

    public String getTime() {
        return time;
    }

    public void setTime(String time) {
        this.time = time;
    }

    public String getTime_Out() {
        return time_Out;
    }

    public void setTime_Out(String time_Out) {
        this.time_Out = time_Out;
    }

    public String getTime_Card_Hours() {
        return time_Card_Hours;
    }

    public void setTime_Card_Hours(String time_Card_Hours) {
        this.time_Card_Hours = time_Card_Hours;
    }

    public String getPay_Cycle_Start_Date() {
        return pay_Cycle_Start_Date;
    }

    public void setPay_Cycle_Start_Date(String pay_Cycle_Start_Date) {
        this.pay_Cycle_Start_Date = pay_Cycle_Start_Date;
    }

    public String getPay_Cycle_End_Date() {
        return pay_Cycle_End_Date;
    }

    public void setPay_Cycle_End_Date(String pay_Cycle_End_Date) {
        this.pay_Cycle_End_Date = pay_Cycle_End_Date;
    }

    public String getEmployee_Name() {
        return employee_Name;
    }

    public void setEmployee_Name(String employee_Name) {
        this.employee_Name = employee_Name;
    }

    public String getFile_Number() {
        return file_Number;
    }

    public void setFile_Number(String file_Number) {
        this.file_Number = file_Number;
    }
}
